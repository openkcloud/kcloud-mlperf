#!/usr/bin/env bash
# 14_collect_results.sh — collect raw artifacts from NFS /mnt/results/ for current RUN_ID.
#
# Actions:
#   1. Copy artifacts from /mnt/results/$RUN_ID/ into results/$RUN_ID/ (local)
#   2. Compute SHA-256 of each artifact
#   3. Write manifest to results/$RUN_ID/manifest.sha256
#
# Read artifacts from NFS if mounted; fall back to copying from kubectl exec.
#
# Usage:
#   ./14_collect_results.sh [--nfs-path <path>] [--dry-run] [--help]
#
# Exit codes:
#   0  collected (or nothing to collect)
#   1  collection failure
#   2  user error

set -euo pipefail

case "${1:-}" in
  --help|-h)
    sed -n '/^#!/d; /^[^#]/q; s/^# \{0,1\}//; p' "$0"
    exit 0
    ;;
esac

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
# shellcheck source=scripts/common.sh
source "$SCRIPT_DIR/common.sh"

NFS_PATH="/mnt/results"
while [ "$#" -gt 0 ]; do
  case "$1" in
    --dry-run)   DRY_RUN=true;    shift ;;
    --nfs-path)  NFS_PATH="$2";   shift 2 ;;
    *) die "Unknown flag '$1'. See --help." 2 ;;
  esac
done

log "=== 14_collect_results ==="
log "RUN_ID=$RUN_ID  DRY_RUN=$DRY_RUN  NFS_PATH=$NFS_PATH"

LOCAL_RESULTS="$REPO_ROOT/results/$RUN_ID"
MANIFEST_FILE="$LOCAL_RESULTS/manifest.sha256"
NFS_RUN_PATH="${NFS_PATH}/${RUN_ID}"

mkdir -p "$LOCAL_RESULTS"

# ---------------------------------------------------------------------------
# 1. Copy artifacts
# ---------------------------------------------------------------------------
log "--- Step 1: collect artifacts from $NFS_RUN_PATH ---"

if [ "$DRY_RUN" = "true" ]; then
  log "  [DRY-RUN] would copy from $NFS_RUN_PATH to $LOCAL_RESULTS"
  log "  [DRY-RUN] would compute SHA-256 manifest"
  log "=== 14_collect_results: DRY-RUN COMPLETE ==="
  exit 0
fi

if [ -d "$NFS_RUN_PATH" ]; then
  log "  NFS path $NFS_RUN_PATH is accessible (direct mount)"
  # Collect all files, preserving subdirectory structure
  find "$NFS_RUN_PATH" -type f | while read -r src; do
    rel="${src#${NFS_RUN_PATH}/}"
    dst="$LOCAL_RESULTS/$rel"
    mkdir -p "$(dirname "$dst")"
    cp "$src" "$dst"
    log "  copied: $rel"
  done
elif [ -d "$NFS_PATH" ]; then
  log "  [WARN] $NFS_RUN_PATH does not exist under NFS mount $NFS_PATH"
  log "  Available run directories:"
  ls "$NFS_PATH" 2>/dev/null | while read -r d; do log "    $d"; done || true
  log "  No artifacts to collect for RUN_ID=$RUN_ID"
else
  log "  [WARN] NFS path $NFS_PATH is not mounted or accessible"
  log "  Attempting to collect from log dir $LOG_DIR instead"

  # Fallback: collect any benchmark output from the log dir
  if [ -d "$LOG_DIR" ]; then
    find "$LOG_DIR" -type f | while read -r src; do
      rel="logs/$(basename "$src")"
      cp "$src" "$LOCAL_RESULTS/$rel"
      log "  collected from logs: $rel"
    done
  else
    log "  Nothing to collect."
  fi
fi

# ---------------------------------------------------------------------------
# 2. SHA-256 manifest
# ---------------------------------------------------------------------------
log "--- Step 2: compute SHA-256 manifest ---"

ARTIFACT_COUNT=0
{
  printf '# manifest.sha256 — generated by 14_collect_results.sh\n'
  printf '# RUN_ID: %s\n' "$RUN_ID"
  printf '# generated_at: %s\n' "$(date -u +%Y-%m-%dT%H:%M:%SZ)"
  printf '#\n'
  printf '# Format: <sha256hash>  <relative_path>\n'
  printf '#\n'
} > "$MANIFEST_FILE"

while IFS= read -r -d '' artifact; do
  rel="${artifact#${LOCAL_RESULTS}/}"
  # skip the manifest itself
  [ "$rel" = "manifest.sha256" ] && continue
  sha="$(sha256sum "$artifact" | awk '{print $1}')"
  printf '%s  %s\n' "$sha" "$rel" >> "$MANIFEST_FILE"
  log "  sha256: $sha  $rel"
  ARTIFACT_COUNT=$((ARTIFACT_COUNT + 1))
done < <(find "$LOCAL_RESULTS" -type f -not -name 'manifest.sha256' -print0 | sort -z)

log "  Manifest written: $MANIFEST_FILE ($ARTIFACT_COUNT artifacts)"

log ""
log "  Local results dir: $LOCAL_RESULTS"
log "=== 14_collect_results: COMPLETE ==="
